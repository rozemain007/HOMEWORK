class User:

    def __init__(self, name, last_name):
        self.name = name
        self.last_name = last_name


    def print_name(self):
        print(self.name)


    
    def print_last_name(self):
        print(self.last_name)


    def print_name_and_last_name(self):
        print(self.name, self.last_name)

